CREATE TABLE newsful_users(
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    email TEXT NOT NULL,
    password VARCHAR NOT NULL
);

CREATE TABLE newsful_saved_articles (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    title TEXT NOT NULL,
    url TEXT NOT NULL,
    image TEXT,
    user_id int REFERENCES newsful_users(id) ON DELETE CASCADE NOT NULL
)